public class DataTypes {
    public static void main(String[] args){
        // primitive DataTypes
        byte b = 100; // -128 to 127 (Size - 1 byte)
        short s = 20000; // -32768 to 32767 (Size - 2 byte)
        int i = 5000; // -2^31 to -2^31 -1 (Size - 4 byte)
        long l = 15000000000l; // Size - 8 byte
        float f = 2.2f; // Size - 4 byte
        double d = 787.4; // Size - 8 byte
        char c = 'r'; // Size - 2 byte
        boolean flag = true; // 1 bit

        // Non-Primitive DataTypes
        int[] arr = {1,2,3,4,5}; // Array
        String str = "Hello World"; // String
    }
}
